<a name="list"><b>1. Listados:</b></a>

Opción que permite emitir los listados o reportes que van para el boletín de la propiedad industrial. Debe colocar el rango de solicitudes que van para el boletín, fecha de publicación, número del boletín y el tipo de listado que desee: solicitadas, concedidas, devueltas forma, devueltas fondo, prioridad extinguida, prioridad extinguida en prensa extemporanea, prioridad extinguida en prensa defectuosa, perimidas por no publicación en prensa, denegadas, desistidas, desistidas por registro, abandonadas, abandonadas por no pago, negadas y oposiciones. Pulse en el boton de "Buscar". <br>
<br>
<a name="orde"><b>2. Listado de Orden de Publicación:</b></a>

Opción que emite el listado con la información que va para el boletín de la orden de publicación. Debe colocar el rango de solicitudes, número de boletín, y el tipo de patente (invencion, mejora, modelo industrial, diseño industrial, de introducción, dibujo industrial, modelo de utilidad y variedades vegetales). Pulse en el boton de "Buscar".<br>
<br>

<a name="reha"><b>3. Listado de Patentes de Rehabilitación:</b></a>
<br>
Opción que emite el listado de las patentes de rehabilitación que van para el boletín. Debe ingresar el rango de las solicitudes y el número de boletín. Pulse en el boton de "Buscar".
<br><br>
<a name="tlis"><b>4. Archivos TXT para Ventura:</b></a>
<br>
Opción que permite generar los archivos necesarios (txt) para la edición del boletín de la propiedad industrial. Debe colocar el rango de solicitudes que van para el boletín, número del boletín y el tipo de listado que desee: solicitadas, concedidas, devueltas forma, devueltas fondo, prioridad extinguida, prioridad extinguida en prensa extemporanea, prioridad extinguida en prensa defectuosa, perimidas por no publicación en prensa, denegadas, desistidas, desistidas por registro, abandonadas, abandonadas por no pago, negadas y oposiciones. Pulse en el boton de "Generar". Toda vez finalizado se generan los archivos en el servidor (/boletin) cada uno identificado con su nombre fecha y hora.
<br><br>
<a name="tord"><b>5. Archivos TXT Orden de Publicación:</b></a>
<br>
Opción que permite generar los archivos necesarios (txt) de la orden de publicación para la edición del boletín. Debe colocar el rango de solicitudes y número de boletín. Pulse en el boton de "Generar". Toda vez finalizado se generan los archivos en el servidor (/boletin) cada uno identificado con su nombre fecha y hora.
<br><br>
<a name="treh"><b>5. Archivos TXT Patentes de Rehabilitación:</b></a>
<br>
Opción que permite generar los archivos necesarios (txt) de las patentes de rehabilitación para la edición del boletín. Debe ingresar el rango de las solicitudes y el número de boletín. Pulse en el boton de "Generar". Toda vez finalizado se generan los archivos en el servidor (/boletin) cada uno identificado con su nombre fecha y hora.
