Presione "<b>Cancelar</b>" cuando se haya equivocado en los Datos para volver a ingresarlos.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.<br>
<br><br>

  <li><a name="pebol"><b>Patentes sin Consignaci&oacute;n de Publicaci&oacute;n en Prensa a Perimir x Boletin:</b></a></li>
Permite la actualizaci&oacute;n al estatus de Perimidas, de todas aquellas solicitudes de patentes con Orden de Publicaci&oacute;n en un Bolet&iacute;n determinado, que NO hayan cumplido con este procedimiento, cambia de Estatus las solicitudes y les anexa un evento de tramite en la Cronolog&iacute;a Administrativa.<br><br>
Se debe colocar el n&uacute;mero del bolet&iacute;n y presionar "<b>Actualizar</b>".<br>
Presione "<b>Cancelar</b>" cuando se haya equivocado en el N&uacute;mero del Bolet&iacute;n para volver a ingresar otro n&uacute;mero.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.<br>
<br><br>

  <li><a name="gener"><b>Generaci&oacute;n de Datos:</b></a></li>
Permite generar los datos de las solicitudes que ser&aacute;n publicadas en el boletin. La informaci&oacute;n queda en una tabla temporal hasta el momento de su actualizaci&oacute;n final en lotes.<br><br>
Se debe colocar el rango de solicitudes, el n&uacute;mero de boletin, el tipo de solicitudes y presionar "<b>Generar</b>".<br>
(Cuando son todas las solicitudes, se coloca un rango: 0000-000000 / 9999-999999)<br><br>
Las solicitudes de Anotaciones Marginales deben ser <a href="#veram">Verificadas</a> previamente para poder ser generadas en este m&oacute;dulo.<br> 
<br><br>

  <li><a name="edici"><b>Edici&oacute;n de Datos Generados:</b></a></li>
Permite desincorporar solicitudes de la tabla temporal donde est&aacute;n los <a href="#gener">datos generados</a> para publicar en el boletin.<br><br>
Se debe colocar el n&uacute;mero de boletin, el tipo de publicaci&oacute;n y presionar "<b>Editar</b>". <br><br>
Una vez dentro, se debe seleccionar la casilla ubicada al lado izquierdo del n&uacute;mero de solicitud que se desea eliminar, colocando el puntero del mouse sobre la misma y presionando el boton izquierdo (inmediatamente aparecer&aacute; el simbolo de chequeado).<br>
Para navegar en el listado se debe hacer uso de los botones: [<b>Siguientes</b>] y [<b>Anteriores</b>] ubicados al fondo.<br><br> 
Al culminar la selecci&oacute;n se debe pulsar [<b>Eliminar</b>] para realizar la eliminaci&oacute;n efectiva de la(s) solicitud(es) seleccionada(s).<br> 
<br><br>

  <li><a name="veram" ><b>Verificaci&oacute;n Anotaciones Marginales:</b></a></li>
Permite la verificaci&oacute;n electr&oacute;nica de las solicitudes de anotaciones Marginales.
Este m&oacute;dulo muestra todas las anotaciones marginales que han sido cargadas en sistema y que no han sido publicadas en Boletin.<br><br>
Se debe seleccionar el tipo de anotaci&oacute;n y presionar "<b>Editar</b>".<br><br>
Una vez dentro, se debe seleccionar la casilla ubicada al lado izquierdo del n&uacute;mero de solicitud que se desea verificar, colocando el puntero del mouse sobre la misma y presionando el boton izquierdo (inmediatamente aparecer&aacute; el simbolo de chequeado).<br>
