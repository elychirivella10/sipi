<a name="list"><b>1. Listados:</b></a>
Opción que permite emitir los listados con la información que va para la emisión del boletín de la propiedad industrial. Debe colocar el rango de solicitudes, fecha de publicación, número del boletín y el tipo de listado que desee: solicitadas, concedidas, concedidas reclasificadas, devueltas forma, devueltas fondo, observadas, caducas, prioridad extinguida, prioridad extinguida en prensa extemporanea, prioridad extinguida en prensa defectuosa, perimidas por no publicación en prensa, desistidas, desistimiento de observación, desistimient de observación con mejor derecho, negadas, cambio de nombre, cambio de domicilio, cesiones, fusiones, licencias, renovaciones, orden de publicación y observadas desistidas de oficio. Pulse en el boton de "Buscar". 
<br><br>
<a name="orde"><b>2. Listado de Orden de Publicaci&oacute;n:</b></a>
Opción que emite el listado con la información que va para el boletín de todas aquellas solicitudes en un rango especifico que se encuentren en estatus 2. Debe colocar el rango de solicitudes y número de boletín. Pulse en el boton de "Buscar".

<br><br>
<a name="ordeb" ><b>3. Orden de Publicación x Bolet&iacute;n:</b></a>
Opción que emite el listado con la información que va para el boletín de aquellas solicitudes con orden de publicación en prensa. Debe colocar el rango de solicitudes, tipo de marca, modalidad y número de boletín. Pulse en el boton de "Buscar".

<br><br>
<a name="obsc"><b>4. Observadas sin Contestaci&oacute;n:</b></a>
Reporte que emite listado con información para el boletín, de las solicitudes que se encuentras observadas sin contestación. Rellene las opciones deseadas: solicitud, boletín o tipo de marca, y pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud,titulo, fecha, tipo, estatus, registro, fecha de registro y n&uacute;mero de planilla.

<br><br>
<a name="desi"><b>5. Listado Desistimiento Anotaciones Marginales:</b></a>
Reporte que emite listado con información para el boletín, de aquellas solicitudes desistidas de renovaciones. Rellene las opciones: número de boletín y tipo de anotación marginal (revonación, cesiones, fusiones, licencias, cambio de nombre y de domicilio); pulse en el boton "Buscar".

<br><br>
<a name="devo"><b>6. Listado Devoluciones de Registros a Publicar:</b></a>
Reporte que emite listado con información para el boletín, de aquellas solicitudes con oficio de devolución a anotación marginal. Rellene las opciones: rango de fechas y usuario; pulse en el boton "Buscar".
<br><br>

<a name="rein"><b>7. Listado Reingresos de Devoluci&oacute;n A.M.:</b></a>
Reporte que emite listado con información para el boletín, de aquellas solicitudes con reingresos de devolución a anotación marginal. Rellene las opciones: rango de fechas y usuario; pulse en el boton "Buscar".
<br><br>

<a name="nega"><b>8. Negadas x Articulo y Literal:</b></a>
Reporte que emite listado con información para el boletín, de aquellas solicitudes que se encuentran negadas, discriminandolas por articulo y literal; solo pulse en el boton "Buscar".
<br><br>

<a name="nega"><b>9. Archivos TXT para Ventura:</b></a>
Reporte que emite los archivos en formato TXT para generar el boletín de la propiedad industrial. Debe colocar el rango de solicitudes, número del boletín y el tipo de solicitudes que desea generar: solicitadas, concedidas, concedidas reclasificadas, devueltas forma, devueltas fondo, observadas, caducas, prioridad extinguida, prioridad extinguida en prensa extemporanea, prioridad extinguida en prensa defectuosa, perimidas por no publicación en prensa, desistidas, desistimiento de observación, desistimiento de observación con mejor derecho, desistimiento de observación por ley. Pulse en el boton de "Generar"; una vez terminado el proceso se generaran los archivos en una carpeta en el servidor.  
<br><br>

<a name="otxt"><b>10. Archivos TXT Orden de Publicación:</b></a>
Reporte que emite los archivos en formato TXT para generar el boletín de la propiedad industrial. Se genera un arhivo con todas aquellas solicitudes en un rango especifico que se encuentren en estatus 2. Debe colocar el rango de solicitudes y número de boletín. Pulse en el boton de "Generar"; una vez terminado el proceso se generaran los archivos en una carpeta en el servidor. 
<br><br>

<a name="ntxt"><b>11. Negadas x Articulo y Literal:</b></a>
Reporte que emite los archivos en formato TXT para generar el boletín de la propiedad industrial. Debe colocar el rango de solicitudes, número del articulo negante, el literal y el número del boletín; pulse en el boton "Generar", una vez terminado el proceso se generaran los archivos en una carpeta en el servidor. 
<br><br>

<a name="ctxt"><b>12. Certificados Elaborados TXT para Ventura:</b></a>
Reporte que emite los archivos en formato TXT para generar el boletín de la propiedad industrial. Se genera un archivo con todos los certificados que se encuentran elaborados en un rango de fecha especifico; pulse en el boton "Generar", una vez terminado el proceso se generaran los archivos en una carpeta en el servidor. 

<br><br>
<a name="amtxt"><b>13. Archivos TXT Anotaciones Marginales:</b></a>
Reporte que emite los archivos en formato TXT para generar el boletín de la propiedad industrial, de aquellas solicitudes desistidas de renovaciones. Rellene las opciones: número de boletín y tipo de anotación marginal (revonación, cesiones, fusiones, licencias, cambio de nombre, de domicilio, desistimientos etc.). Pulse en el boton "Generar"; una vez terminado el proceso se generaran los archivos en una carpeta en el servidor.

<br><br>
<a name="dtxt"><b>14. Archivos TXT Devoluciones de Registros a Publicar :</b></a>
Reporte que emite los archivos en formato TXT para generar el boletín de la propiedad industrial, de aquellas solicitudes con oficio de devolución a anotación marginal. Rellene las opciones: rango de fechas y usuario; pulse en el boton "Generar", una vez terminado el proceso se generaran los archivos en una carpeta en el servidor.


<br><br>
<a name="rtxt"><b>15. Archivos TXT Reingreso de Devoluci&oacute;n A.M.:</b></a>
Reporte que emite los archivos en formato TXT para generar el boletín de la propiedad industrial, de aquellas solicitudes con reingresos de devolución a anotación marginal. Rellene las opciones: rango de fechas y usuario; pulse en el boton "Generar", una vez terminado el proceso se generaran los archivos en una carpeta en el servidor.
