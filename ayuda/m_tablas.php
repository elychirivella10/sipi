Para manipular el cuadro de Poderhabientes se debe ingresar el nombre del agente en la casilla ubicada en la esquina inferior izquierda; si se sabe cual es su c&oacute;digo se puede colocar el n&uacute;mero del mismo precedido del signo menos "-" (Ej. -500); y presionar [<b>Buscar/Incluir</b>], inmediatamente aparecer&aacute; una lista de las coincidencias encontradas; se debe seleccionar el nombre a incluir marcando la casilla que est&aacute; a su lado izquierdo; en caso de ser un agente nuevo debe ser ingresado previamente por la opción de: [<b>Mantenimiento-Tablas generales-Ingresos-Agentes</b>]; seguidamente presionar el boton [<b>Incluir</b>] que est&aacute; al final. Para navegar en la lista se utilizar&aacute;n los botones [<b>Previos</b>] y [<b>Siguientes</b>]. Para eliminar un poderhabiente asociado por error, se debe pulsar el boton [<b>Buscar/Eliminar</b>], seleccionar de la lista el agente a eliminar y presionar el boton [<b>Eliminar</b>].<br><br>
