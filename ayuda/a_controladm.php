  <li><a name="evind"><b>Evento Individual:</b></a></li>
Permite cargar un evento a una solicitud o registro en particular dependiendo del estatus en que se encuentre.<br><br>
Se debe ingresar el N&uacute;mero de Solicitud o Registro y presionar "<b>Buscar</b>"; inmediatamente se mostrar&aacute;n los datos del expediente.<br><br> 
Tendra dos opciones para cargar el evento: una es colocando el C&oacute;digo y la otra es buscandolo de una lista desplegable.<br><br>
Solo se mostraran todos aquellos eventos No Migrables y aquellos aplicables solo al estatus actual del expediente.<br><br>
Luego se debe presionar "<b>Continuar</b>" (imagen de color verde con una flecha blanca) para mostrar los datos del evento a cargar.<br><br>
La fecha del evento es obligatoria. Una vez cargados los datos asociados al evento, Para finalizar se debe presionar "<b>Guardar</b>" (Imagen de un diskette) para registrar los datos en el sistema.<br>
El icono Cronolog&iacute;a (imagen de una carpeta) permite visualizar la Cronolog&iacute;a Administrativa de la Obra.<br><br>
Presione "<b>Cancelar</b>" cuando se haya equivocado en el N&uacute;mero de Expediente para volver a ingresar otro n&uacute;mero.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.<br>
<br><br>

  <li><a name="devol"><b>Devoluciones:</b></a></li>
Permite registrar el evento de devoluci&oacute;n que es aplicado a los Expedientes cuando no cumplen con ciertos requisitos exigidos por la Ley de Propiedad Industrial.<br><br>
Los datos que aqui se almacenan sirven de insumo para la emisi&oacute;n del Oficio de Devoluci&oacute;n.<br><br>
Se debe ingresar el n&uacute;mero de expediente y presionar "<b>Buscar</b>"; inmediatamente se mostrar&aacute;n los datos de la solicitud; luego se puede introducir la fecha del evento, aunque por defecto el sistema coloca la fecha del dia.<br><br>
Despu&eacute;s se marca uno o m&aacute;s causales de devoluci&oacute;n (colocar el puntero del mouse en la casilla del lado izquierdo del enunciado y presionar el boton izquierdo del mismo para que aparezca el simbolo de chequeo). Si la causa no est&aacute; estipulada en los enunciados, se debe llenar el campo "<b>Otro</b>".<br><br>
Para finalizar se debe presionar "<b>Guardar</b>" (imagen de un diskette) para registrar los datos en el sistema.<br>
El icono Cronolog&iacute;a (imagen de una carpeta) permite visualizar la Cronolog&iacute;a Administrativa del Expediente.<br>
<br><br>

  <li><a name="regis"><b>Asignar N&uacute;mero de Registro:</b></a></li>
Permite asignar el N&uacute;mero de Registro a una Solicitud que haya pagado los Derechos, es decir que este en estatus 410.<br><br>
Se debe ingresar el N&uacute;mero de Solicitud y presionar "<b>Buscar</b>"; inmediatamente se mostrar&aacute;n los datos del expediente.<br><br> 
Luego procedera a colocar la Fecha de Vigencia y Nro de Registro.<br><br>
Para finalizar se debe presionar "<b>Guardar</b>" (imagen de un diskette) para registrar los cambios en el sistema de manera inmediata.<br><br>
El icono Cronolog&iacute;a (imagen de una carpeta) permite visualizar la Cronolog&iacute;a Administrativa de la Obra.<br><br>
Presione "<b>Cancelar</b>" cuando se haya equivocado en el N&uacute;mero de Expediente para volver a ingresar otro n&uacute;mero.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.<br>
