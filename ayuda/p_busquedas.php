<a name="cargar"><b>a. Cargar:</b></a>
<br>
Módulo que permite realizar la búsqueda de peticionario o titulares. En primer lugar en la pantalla se muestra un <b>Código de la Palabra</b>, este sera el número con el que podra buscar el reporte o consulta del peticionario en la opción de <b>Reporte</b>. Posteriormente debe colocar el nombre completo del titular a buscar y por ultimo la palabra del titular buscar; esta palabra debe ser de 6 digitos y que se encuentre contenida en el nombre del titular (los mas idoneo es colocar las palabras menos comunes para que los resultados sean mas concretos). Pulse en el boton "Editar".
<br><br>
Posteriormente le muestra un listado con los nombres de los titulares exactos o parecidos a los solicitados por usted, tilde o marque los que sean iguales al titular solicitado en la <b> Inc </b>, despues de terminar de revisar el listado pulse el boton de "Incluir". Le dara un mensaje de datos incluidos correctamente.
<br><br>
<a name="repor"><b>a. Reporte:</b></a>
<br>
Módulo que permite emitir el reporte de la búsqueda de peticionario o titulares. Indique el numero de recibo que posee la planilla de pago, fecha del día, número de peticionario (número que aparecia en la opcion de Cargar en <b>Código de la Palabra</b>), nombre del titular, nombre del solicitante y el tipo de peticionario (A = en línea o habilitada /  B= normal). Pulse en el boton "Buscar".
