<a name="tecn"><b>1. Técnica:</b></a>
Sistema de busqueda de patentes que permite realizar consultas de las patentes por: titulo, solicitud, titular, clasificación internacional, clasificación locarno, resumen, inventor, prioridad, tramitante, registro, por fecha de presentación, por fecha de publicación y por país. Es importante recalcar que en los campo de formato texto, puede seleccionar la forma en que lo desea consultar (que contenga la porción del texto, que sea exactamente igual y que comience por).
<br><br>
Ademas exite una opción denominada "Combinada": que  permite realizar las consultas anteriores pero con la posibilidad de unir los campos para realizar sus propias búsquedas de la forma en que desee. 
<br><br>
Después de realizar la consulta, le mostrara una lista con los resultados, para mas detalle pulse en el numero de la solicitud y le mostrara los datos de la misma, la cronologia de la solicitud, la memoria descriptiva y la ficha.
