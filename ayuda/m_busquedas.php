<a name="bexte"><b>g. Búsqueda Externa:</b></a>
<br>
Módulo que permite realizar la búsqueda externa de elementos figurativo a través de la comparación de los Códigos de Viena y Clase Internacional de Niza.<br><br> 
Se debe indicar el número de pedido y presionar "<b>Buscar</b>"; inmediatamente se mostrar&aacute;n los datos del pedido.<br><br>
Se debe ingresar la Clase Internacional de Niza y tantos nombres o c&oacute;digos en la casilla ubicada justo al lado del bot&oacute;n [<b>Buscar/Incluir</b>] y presionar [<b>Buscar/Incluir</b>] o [<b>Buscar/Eliminar</b>] dependiendo de la acci&oacute;n a realizar, si es el primer bot&oacute;n inmediatamente aparecer&aacute; una lista de las coincidencias encontradas; se debe seleccionar la descripción a incluir marcando la casilla que est&aacute; a su lado izquierdo seguidamente presionar el boton [<b>Grabar</b>] que est&aacute; al final. Para navegar en la lista se utilizar&aacute;n los botones [<b>Previos</b>] y [<b>Siguientes</b>]. Para eliminar un código asociado por error, se debe pulsar el bot&oacute;n [<b>Buscar/Eliminar</b>], y seleccionar o hacer Clic en el cuadrito que aparece en la primera columna debajo de Sel y presionar el bot&oacute;n "<b>Eliminar</b>".<br><br>
Para iniciar el proceso de comparación se debe presionar el boton "<b>Continuar</b>" (imagen de color verde con una flecha blanca).<br><br> 
Una vez finalizado el proceso de comparación se mostrará cuantas solicitudes fueron verificadas y el total de posibles elementos que hacen match con lo buscado.<br><br>
Se debe presionar el botón "<b>Comparar</b>" para visualizar todas aquellas imagenes de las solicitudes que hicieron match, para seleccionar de manera visual aquellas que posiblemente tengan un alto porcentaje de parecido. 
Para seleccionar la imagen solo hay que hacer Clic en la casilla justo al lado de la palabra "<b>Sel.</b>".
Para navegar en la lista se utilizar&aacute;n los botones [<b>Anteriores 12</b>] y [<b>Siguientes 12</b>]. 
Cuando se hayan seleccionado todas las posibles similitudes, se debe presionar el botón "<b>Imprimir</b>", y luego la opcion que corresponda bien para imprimir el reporte con o sin el resultado obtenido del proceso visual realizado.
Presione "<b>Cancelar</b>" cuando se haya equivocado en los datos ingresados.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.<br><br>
