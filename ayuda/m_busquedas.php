<a name="figu"><b>1. Figurativa:</b></a>
Sistema de búsqueda que permite realizar consultas de las Marcas Gráficas y Mixtas con Logotipos a traves de los Codigos de Viena asociados a la imagen y filtrado por Clase Internacional y sus Clases Asociadas. 
<br><br>
<a name="exter"><b>a. Copiar Imagen Externa al Servidor:</b></a>
<br>
Módulo que permite realizar la copia de una imagen relacionada a la solicitud de una búsqueda externa al Servidor.<br><br>
Primero debe buscar la imagen scaneada y guardada en su maquina local por medio del botón <b>Examinar</b>, y posteriormente debe colocar en Logotipo el numero de factura o pedido correspondiente.<br><br>
Para finalizar se debe presionar "<b>Guardar</b>" (imagen de un diskette) para registrar los cambios en el sistema de manera inmediata.<br><br>
Presione "<b>Cancelar</b>" cuando se haya equivocado en los datos para volver a ingresarlos.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.<br><br>
<a name="inter"><b>b. Copiar Imagen Fondo al Servidor:</b></a>
<br>
