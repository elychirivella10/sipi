Se debe ingresar el N&uacute;mero de Pedido y presionar "<b>Buscar</b>"; inmediatamente se mostrar&aacute;n los datos del pedido y podra modificar aquel donde este el error.<br><br> 
Para finalizar se debe presionar "<b>Guardar</b>" (imagen de un diskette) para registrar los cambios en el sistema de manera inmediata.<br><br> 
