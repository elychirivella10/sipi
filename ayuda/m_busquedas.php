<a name="verif"><b>d. Actualización de Verificación:</b></a>
<br>
Módulo que permite ingresar o corregir la descripción del logotipo o etiqueta, así como clasificar la imagen a través de los Códigos de Viena.<br><br>
Se debe ingresar el N&uacute;mero de Solicitud y presionar "<b>Buscar</b>"; inmediatamente se mostrar&aacute;n los datos b&aacute;sicos del expediente.<br><br> 
Luego debe presionar "<b>Actualizar</b>" en el que se podrá modificar o incluir la descripción de la etiqueta e ir colocando los códigos de viena.<br><br>
Se debe ingresar el nombre o c&oacute;digo en la casilla ubicada justo al lado del bot&oacute;n [<b>Buscar/Incluir</b>] y presionar [<b>Buscar/Incluir</b>] o [<b>Buscar/Eliminar</b>] dependiendo de la acci&oacute;n a realizar, si es el primer bot&oacute;n inmediatamente aparecer&aacute; una lista de las coincidencias encontradas; se debe seleccionar la descripción a incluir marcando la casilla que est&aacute; a su lado izquierdo seguidamente presionar el boton [<b>Grabar</b>] que est&aacute; al final. Para navegar en la lista se utilizar&aacute;n los botones [<b>Previos</b>] y [<b>Siguientes</b>]. Para eliminar un código asociado por error, se debe pulsar el bot&oacute;n [<b>Buscar/Eliminar</b>], y seleccionar o hacer Clic en el cuadrito que aparece en la primera columna debajo de Sel y presionar el bot&oacute;n "<b>Eliminar</b>".<br><br>
Presione "<b>Cancelar</b>" cuando se haya equivocado en el N&uacute;mero de Expediente para volver a ingresar otro n&uacute;mero.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.<br><br>
<a name="ingpe"><b>e. Ingreso de Pedido:</b></a>
<br>
Módulo que permite ingresar toda la información relacionada al pedido de la Búsqueda Externa de elemento figurativo. El número de pedido es generado automáticamente el cual debe ser escrito en el pedido a manera de control. El tipo de busqueda puede ser (A = en línea o habilitada /  B= Normal) y debe escanearse la imagen o logotipo a buscar y asociarse al pedido generado.<br><br>
Para finalizar se debe presionar "<b>Guardar</b>" (imagen de un diskette) para registrar los cambios en el sistema de manera inmediata.<br><br> 
Presione "<b>Cancelar</b>" cuando se haya equivocado en el N&uacute;mero de Expediente para volver a ingresar otro n&uacute;mero.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.<br><br>
