<a name="abie"><b>1. Consulta Abierta:</b></a>

Permite realizar consultas por un rango de solicitudes, escriba los datos correspondientes y pulse en el boton de "Buscar". Le mostrara el detalle de cada solicitud, con información que no se encuentra en la cronologia.<br>
<br>
<a name="crono"><b>2. Cronologia de la solicitud:</b></a>

Permite realizar consultas por el numero de la solicitud o por el numero de registro. escriba los datos correspondientes y pulse en el boton de "Buscar".<br>
<br>

<a name="certi"><b>3. Certificados:</b></a>
<br>
Para imprimir los certificados se debe colocar el rango o el numero de registro que desea emitir, pulse el boton "Buscar" y le mostrara el certificado en pantalla para su posterior impresión.
<br><br>
<a name="pdf"><b>4. Documentos PDF:</b></a>
<br>
Opcion que permite emitir la pagina de inicio de las memorias descriptivas de las patentes. Debe colocar el número de la solicitud que desea y pulse "Buscar".
<br><br>
<a><b>5. Administrativos:</b></a>
<br><br>
<a name="soli" ><b>a. Solicitudes Presentadas:</b> </a>
<br>
Reporte que permite obtener el listado de las solicitudes cargadas por un usuario en un rango de fecha. Los campos que se muestran en el reporte son: solicitud, Nombre, fecha, tipo, estatus y titular. <br>
<br>

<a name="ofic"><b>b. Oficio de Devoluci&oacute;n:</b> </a>
<br>
Reporte que permite emitir los oficios de devolución correspondiente a una solicitud o a un rango. Coloque el rango de las solicitudes deseadas, seleccione el estatus que desea (200 ó 202)y pulse en el boton "Buscar".<br>
<br><br>
<a><b>4. Usuarios:</b></a>
<br><br>
<a name="crit"><b>a. Por Criterio:</b></a>
Reporte que permite buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: solicitud, registro, fecha de solicitud, fecha de publicación, fecha de vencimiento, estatus, tipo de patente, país, titulo de la patente, código del titular, código del agente o nombre del tramitante. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, nombre, fecha de solicitud, tipo, estatus, registro, fecha de registro y fecha de vencimiento.
<br><br>

<a name="tran"><b>c. Por Transacciones:</b></a>

Reporte que permite buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: rango de fechas de transacciones y eventos, evento, usuario, estatus o boletin. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, titulo, evento, estatus previo, fecha de evento, fecha de transacci&oacute;n, estatus actual y número de boletín.<br><br>

<a name="trar"><b>c. Por Transacciones x Recept.:</b></a>

Reporte que permite buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: rango de fechas de transacciones y eventos, evento, usuario, estatus o boletin. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, titulo, evento, estatus previo, fecha de evento, fecha de transacci&oacute;n, estatus actual y número de boletín. Muestra todos los eventos cargados por solicitud.<br><br>

<a><b>5. Tablas:</b></a>
<br><br>
<a name="stat"><b>a. Status Administrativos:</b></a>
Opci&oacute;n que emite un listado de todos los estatus administrativos con su respectiva descripci&oacute;n, existentes para las patentes. 
<br><br>
<a name="even"><b>b. Eventos:</b></a>
Opci&oacute;n que emite un listado de todos los eventos administrativos con su respectiva descripci&oacute;n, existentes para las patentes. En el listado se pueden observar los campos de evento, descripci&oacute;n, tipo, mensaje descriptivo del evento, plazo, tipo de plazo y comentario.
<br><br>
<a name="migr"><b>c. Migración de Estatus:</b></a>
Opci&oacute;n que muestra  un listado de las posibles migraciones de estatus a travez de los eventos para una solicitud. En el listado se pueden observar los campos de evento, descripci&oacute;n y migraci&oacute;n desde - hasta.
