<a name="inte"><b>1. Consulta Interna:</b></a>

Permite realizar consultas por número de solicitud, número de registro o por nombre de la marca; escriba los datos correspondientes y pulse en el boton de "Buscar". Le mostrara el listado con los resultados, posteriormente pulse el número de solicitud que desea para los detalles de la misma. <br>
<br>
<a name="abie"><b>2. Consulta Abierta:</b></a>

Permite realizar consultas por un rango de solicitudes, escriba los datos correspondientes y pulse en el boton de "Buscar". Le mostrara el detalle de cada solicitud, con información que no se encuentra en la cronologia.<br>
<br>
<a name="crono"><b>3. Cronologia:</b></a>

Permite realizar consultas por el número de la solicitud o por el numero de registro. escriba los datos correspondientes y pulse en el boton de "Buscar".<br>
<br>
<a name="pode"><b>4. Poderes:</b></a>

Permite realizar la consulta de poderes de marcas y patentes. Introduzca el número de la solicitud. escriba los datos correspondientes y pulse en el boton de "Buscar". Le mostrara la fecha del poder, facultad (marcas, patentes o ambas), titulares y poderhabientes.<br>
<br>
<a name="tasa"><b>5. Pago de Tasa:</b></a>

Permite realizar la consulta de los pagos de tasas de derecho. Introduzca el rango de fechas a consultar y el evento que desea (65 ó 66) y pulse en el boton de "Buscar". Le mostrara un reporte con la fecha de transacción, número de recibo, fecha del evento y número de la solicitud, nombre y clase de las marcas correspondientes al pago.<br><br>
<a><b>6. Certificados:</b></a>
<br><br>
<a name="sold"><b>a. Solo Datos:</b></a>
<br>
Para imprimir los certificados se debe colocar el rango o el número de registro que desea emitir, pulse el boton "Buscar" y le mostrara el certificado en pantalla para su posterior impresión.
<br><br>
<a name="cond"><b>b. Cont. Datos:</b></a>
<br>
Opción que permite imprimir la continuación de los datos (titulares, distingue y etiqueta) de los certificados que no salieron completos. Igualmente se debe colocar el rango o el número de registro que desea emitir, pulse el boton "Buscar" y le mostrara la información en pantalla para su impresión en la parte posterior del certificado.
<br><br>
<a name="cere"><b>c. Certif.Elaborados:</b></a>
<br>
Reporte que emite los certificados que se encuentran elaborados; debe colocar el rango de fechas que desea y el nombre del usuario. Emite un listado con los campos de número de registro, número de solicitud, clase, nombre de la marca, titular y número de boletín. 
<br><br>
<a><b>7. Administrativos:</b></a>
<br><br>
<a name="soli" ><b>a. Solicitudes Presentadas:</b> </a>
<br>
Reporte que permite obtener el listado de las solicitudes cargadas por un usuario en un rango de fecha. Los campos que se muestran en el reporte son: solicitud, Nombre, fecha, clase, tipo, modalidad, estatus y titular. <br>
<br><a name="ofic"><b>b. Oficio de Devoluci&oacute;n":</b> </a>
<br>
Reporte que permite emitir los oficios de devolución correspondiente a una solicitud o a un rango. Coloque el rango de las solicitudes deseadas y pulse en el boton "Buscar".<br>
<br><br>
<a><b>8. Usuarios:</b></a>
<br><br>
<a name="crit"><b>a. Por Criterio:</b></a>
Reporte que permite buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: solicitud, registro, fecha de solicitud, fecha de publicación, fecha de vencimiento, estatus, tipo de marca, clase, país, nombre de la marca, código del titular, código del agente o nombre del tramitante. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, nombre, fecha de solicitud, tipo, clase, estatus, registro, fecha de registro y fecha de vencimiento.
<br><br>
<a name="lisv"><b>b. Por listados varios:</b></a>
Reporte que permite buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: rango de fechas de transacciones y eventos, evento, usuario, estatus, boletin o modalidad. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, nombre, modalidad, clase, evento, estatus previo, fecha de evento, fecha de transacci&oacute;n, fecha de vencimiento, estatus actual y número de boletín; ordenado por el número de solicitud.<br><br>
<a name="lisvr"><b>c. Por listados varios x Registro:</b></a>
Reporte que permite buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: rango de fechas de transacciones y eventos, evento, usuario, estatus y boletin. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, nombre, modalidad, clase, evento, estatus previo, fecha de evento, fecha de transacci&oacute;n, fecha de vencimiento, estatus actual y número de boletín; ordenado por el número de registro.<br><br>
<a name="mxbol"><b>d. Listado Marcas Registradas X Bol.:</b></a>
Opción que permite emitir el listado de las marcas registradas por boletín, indique el tipo de marca (marca de producto, nombre comercial, lema comercial, marca de servicio, marca colectiva, denominación de origen), el número del boletín y la forma en la que desea ordenado el listado por solicitud o registro. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, nombre, modalidad, clase, evento, estatus previo, fecha de evento, fecha de transacci&oacute;n, fecha de vencimiento, estatus actual y número de boletín; ordenado por el número de registro.<br><br>

<a name="ltit"><b>e. Listados con Titulares:</b></a>
Reporte que permite buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: rango de fechas de transacciones y eventos, evento, usuario, estatus o boletin. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, fecha de solicitud, nombre, modalidad, clase, estatus y titular.<br><br>

<a name="tran"><b>f. Por Transacciones x Sol.:</b></a>
Reporte que permite consultar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: rango de fechas de transacciones y eventos, evento o usuario. Pulse en el boton "Buscar"; le mostrara un reporte con los siguientes campos: solicitud, nombre, modalidad, clase, evento, estatus previo, fecha de evento, fecha de vencimiento, fecha de transacci&oacute;n, estatus actual y número de boletín.<br><br>

<a name="trar"><b>g. Por Transacciones x Reg.:</b></a>
Reporte que permite buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: rango de fechas de transacciones y eventos, evento, tipo de marca o usuario. Pulse en el boton "Buscar"; le mostrara un reporte ordenado por numero de registro, con los siguientes campos: registro, solicitud, nombre, modalidad, clase, evento, estatus previo, fecha de evento, fecha de vencimiento, fecha de transacci&oacute;n, estatus actual y número de boletín.<br><br>

<a name="anot"><b>h. Listado de Anotaciones Marginales a Publicar:</b></a>
Reporte que permite consultar las anotaciones marginales (cambio de titular, cambio de domicilio, licencias de uso, fusiones, cesiones y renovaciones); se puede buscar por diferentes campos combinados o indivuales. Rellene las opciones deseadas: rango de fechas de la solicitud, tipo de anotaci&oacute;n y usuario. Pulse en el boton "Buscar"; le mostrara un reporte ordenado por numero de registro, con los siguientes campos: registro, tipo de anotaci&oacute;n, fecha de transacci&oacute;n, documento, vencimiento de renovaci&oacute;n, titular anterior, titular nuevo, domicilio anterior, domicilio nuevo, nacionalidad, y tramitante.
<br><br>

<a><b>9. Tablas:</b></a>
<br><br>
<a name="stat"><b>a. Status Administrativos:</b></a>
Opci&oacute;n que emite un listado de todos los estatus administrativos con su respectiva descripci&oacute;n, existentes para las marcas. 
<br><br>
<a name="even"><b>b. Eventos:</b></a>
Opci&oacute;n que emite un listado de todos los eventos administrativos con su respectiva descripci&oacute;n, existentes para las marcas. En el listado se pueden observar los campos de evento, descripci&oacute;n, tipo, mensaje descriptivo del evento, plazo, tipo de plazo y comentario.
<br><br>
<a name="migr"><b>c. Migración de Estatus:</b></a>
Opci&oacute;n que muestra  un listado de las posibles migraciones de estatus a travez de los eventos para una solicitud. En el listado se pueden observar los campos de evento, descripci&oacute;n y migraci&oacute;n desde - hasta.
<br><br>
<a name="pais"><b>d. Países:</b></a>
Opci&oacute;n que muestra  un listado de todos los paises y su respectivas nacionalidades, cargados en el sistema de propiedad intelectual.
<br><br>
<a name="agen"><b>e. Agentes:</b></a>
Opci&oacute;n que muestra  un listado de todos los agentes existentes en el sistema de propiedad intelectual.
<br><br>
<a name="pode"><b>f. Poderes:</b></a>
Opci&oacute;n que permite consultar los poderes, coloque el rango del poder o el rango de la fecha de transacci&oacute;n. Pulse en el boton "Buscar"; le mostrara un listado con el numero de poder, fecha del poder, facultad, titular asociado y los poderhabientes.
<br><br>
<a name="inter"><b>g. Clases Internacionales:</b></a>
