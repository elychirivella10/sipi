  <li><a name="evind"><b>Evento Individual:</b></a></li>
Permite cargar un evento a una solicitud o registro en particular dependiendo del estatus en que se encuentre.<br><br>
Se debe ingresar el N&uacute;mero de Solicitud o Registro y presionar "<b>Buscar</b>"; inmediatamente se mostrar&aacute;n los datos del expediente.<br><br> 
Tendra dos opciones para cargar el evento: una es colocando el C&oacute;digo y la otra es buscandolo de una lista desplegable.<br><br>
Solo se mostraran todos aquellos eventos No Migrables y aquellos aplicables solo al estatus actual del expediente.<br><br>
Luego se debe presionar "<b>Continuar</b>" (imagen de color verde con una flecha blanca) para mostrar los datos del evento a cargar.<br><br>
La fecha del evento es obligatoria. Una vez cargados los datos asociados al evento, Para finalizar se debe presionar "<b>Guardar</b>" (Imagen de un diskette) para registrar los datos en el sistema.<br>
El icono Cronolog&iacute;a (imagen de una carpeta) permite visualizar la Cronolog&iacute;a Administrativa de la Patente.<br><br>
Presione "<b>Cancelar</b>" cuando se haya equivocado en el N&uacute;mero de Expediente para volver a ingresar otro n&uacute;mero.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.
<br><br>
  <li><a name="evlot"><b>Evento por Lotes:</b></a></li>
Permite cargar un determinado evento en lote, es decir a varias solicitudes dependiendo de los criterios seleccionados a buscar. <br><br>
Se debe ingresar cualquiera de los siguientes valores para realizar la b&uacute;squeda: (Rango de Solicitudes, Evento, Estatus Actual, Estatus Anterior, Usuario, Fecha de Transacción, Fecha de Evento y/o Documento); colocar el C&oacute;digo del Evento a aplicar y presionar "<b>Continuar</b>" (imagen de color verde con una flecha blanca).<br><br>
Deber&aacute; colocar la Fecha del Evento y, la Fecha de Publicaci&oacute;n o de Vencimiento dependiendo del evento, luego presionar "<b>Continuar</b>" (Imagen de color verde con una flecha blanca). <br><br>
La fecha del evento es obligatoria. Una vez cargados los datos asociados al evento, Para finalizar se debe presionar "<b>Actualizar</b>" (Imagen o bot&oacute;n de color azul con un check) para registrar los datos en el sistema.<br>
Presione "<b>Cancelar</b>" cuando se haya equivocado en los datos para volver a ingresarlos.<br><br>
Presione "<b>Salir</b>" cuando desee salir de la pantalla.
<br><br>
  <li><a name="devol"><b>Devoluciones:</b></a></li>
Permite registrar el evento de devoluci&oacute;n que es aplicado a las Patentes cuando no cumplen con ciertos requisitos exigidos por la Ley de Propiedad Industrial.<br><br>
Estas devoluciones solo pueden ser aplicadas a las patentes que est&aacute;n en <b>Estatus 1</b> (Solicitud Presentada). Los datos que aqui se almacenan sirven de insumo para la emisi&oacute;n del Oficio de Devoluci&oacute;n.<br><br>
Se debe ingresar el n&uacute;mero de solicitud y presionar "<b>Buscar</b>"; inmediatamente se mostrar&aacute;n los datos de la solicitud; luego se puede introducir la fecha del evento, aunque por defecto el sistema coloca la fecha del dia.<br><br>
Despu&eacute;s se marca uno o m&aacute;s causales de devoluci&oacute;n (colocar el puntero del mouse en la casilla del lado izquierdo del enunciado y presionar el boton izquierdo del mismo para que aparezca el simbolo de chequeo). Si la causa no est&aacute; estipulada en los enunciados, se debe llenar el campo "<b>Otro</b>".<br><br>
Para finalizar se debe presionar "<b>Guardar</b>" (imagen de un diskette) para registrar los datos en el sistema.<br>
El icono Cronolog&iacute;a (imagen de una carpeta) permite visualizar la Cronolog&iacute;a Administrativa de la Patente.<br>
<br><br>

  <li><a name="cagen"><b>Cambios de Agente/Tramitante:</b></a></li>
Permite realizar el cambio de Agente o Tramitante de una solicitud de Patentes que se encuentra en tramite, es decir, que no se le ha asignado n&uacute;mero de registro.<br><br>
Se debe ingresar el n&uacute;mero de solicitud y presionar "<b>Buscar</b>", luego colocar la fecha del evento (la que aparece en la hoja de solicitud) y el n&uacute;mero de documento (debe ser un n&uacute;mero &uacute;nico como el del expediente).<br><br>
Despu&eacute;s, introducir el nombre del nuevo tramitante o asociar el nuevo Agente (se puede asociar m&aacute;s de uno). Para manipular el cuadro de agentes se debe ingresar el nombre del agente en la casilla ubicada en la esquina inferior izquierda; si se sabe cual es su c&oacute;digo se puede colocar el n&uacute;mero del mismo precedido del signo menos "-" (Ej. -500); y presionar [<b>Buscar/Incluir</b>], inmediatamente aparecer&aacute; una lista de las coincidencias encontradas; se debe seleccionar el nombre a incluir marcando la casilla que est&aacute; a su lado izquierdo; en caso de ser un agente nuevo debe ser ingresado previamente por la opción de: [<b>Mantenimiento-Tablas generales-Ingresos-Agentes</b>]; seguidamente presionar el boton [<b>Incluir</b>] que est&aacute; al final. Para navegar en la lista se utilizar&aacute;n los botones [<b>Previos</b>] y [<b>Siguientes</b>]. Para eliminar un agente asociado por error, se debe pulsar el boton [<b>Buscar/Eliminar</b>], seleccionar de la lista el agente a eliminar y presionar el boton [<b>Eliminar</b>].<br><br>
Para finalizar se debe presionar "<b>Guardar</b>" (imagen de un diskette) para registrar los cambios en el sistema de manera inmediata.<br>
