Despu&eacute;s se deben colocar los titulares o el titular resultante del cambio. Si el titular original se mantiene, debe ser registrado en este cuadro. Cabe destacar que el titular o los titulares que se muestran en el cuadro "<b>Titular(es) Actual(es)</b>" desaparecen (al momento de guardar) y quedar&aacute;n registrados los que se indiquen en el cuadro "<b>Titular(es) Final(es)</b>". Para manipular este cuadro se debe ingresar el nombre del titular en la casilla ubicada en la esquina inferior izquierda y presionar [<b>Buscar/Incluir</b>], inmediatamente aparecer&aacute; una lista de las coincidencias encontradas; se debe seleccionar el nombre a incluir marcando la casilla que est&aacute; a su lado izquierdo e ingresar la nacionalidad y el domicilio; en caso de ser un titular nuevo no aparecer&aacute; la lista y se deben ingresar los datos por primera vez; seguidamente presionar el boton [<b>Incluir</b>] que est&aacute; al final. Para navegar en la lista se utilizar&aacute;n los botones [<b>Siguientes</b>] y [<b>Previos</b>]. Para eliminar un titular asociado por error, se debe pulsar el boton [<b>Buscar/Eliminar</b>], seleccionar de la lista el titular a eliminar y presionar el boton [<b>Eliminar</b>].<br><br>
